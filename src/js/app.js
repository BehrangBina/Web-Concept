const progress = document.querySelector(".scroll-progress");
const revealItems = [...document.querySelectorAll(".reveal")];
const menuToggle = document.querySelector(".menu-toggle");
const siteMenu = document.querySelector(".site-menu");
const navLinks = [...document.querySelectorAll(".site-menu a")];
const railLinks = [...document.querySelectorAll(".story-rail a")];
const panels = [...document.querySelectorAll(".story-panel[data-story]")];
const languageButtons = [...document.querySelectorAll("[data-lang]")];
const navGroups = [...document.querySelectorAll("[data-menu-group]")];
const defaultLanguage = "fa";

function clamp(value, min, max) {
  return Math.min(Math.max(value, min), max);
}

function getTranslation(dictionary, key) {
  return dictionary[key] || translations[defaultLanguage][key] || "";
}

function applyLanguage(language) {
  const selectedLanguage = translations[language] ? language : defaultLanguage;
  const dictionary = translations[selectedLanguage];
  const config = languageConfig[selectedLanguage];
  const description = document.querySelector('meta[name="description"]');

  document.documentElement.lang = config.htmlLang;
  document.documentElement.dir = config.dir;
  document.body.dataset.lang = selectedLanguage;
  document.title = getTranslation(dictionary, "meta.title");

  if (description) {
    description.setAttribute("content", getTranslation(dictionary, "meta.description"));
  }

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    element.textContent = getTranslation(dictionary, key);
  });

  document.querySelectorAll("[data-i18n-aria-label]").forEach((element) => {
    const key = element.dataset.i18nAriaLabel;
    element.setAttribute("aria-label", getTranslation(dictionary, key));
  });

  document.querySelectorAll("[data-i18n-alt]").forEach((element) => {
    const key = element.dataset.i18nAlt;
    element.setAttribute("alt", getTranslation(dictionary, key));
  });

  languageButtons.forEach((button) => {
    const isActive = button.dataset.lang === selectedLanguage;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  try {
    localStorage.setItem("preferredLanguage", selectedLanguage);
  } catch (error) {
    // Local storage can be disabled in strict privacy modes; the switcher still works for the current page.
  }
}

function getInitialLanguage() {
  try {
    const savedLanguage = localStorage.getItem("preferredLanguage");
    if (savedLanguage && translations[savedLanguage]) return savedLanguage;
  } catch (error) {
    // Ignore storage access errors and keep Persian as the default language.
  }

  return defaultLanguage;
}

function updateProgress() {
  if (!progress) return;

  const scrollable = document.documentElement.scrollHeight - window.innerHeight;
  const ratio = scrollable > 0 ? window.scrollY / scrollable : 0;
  progress.style.transform = `scaleX(${clamp(ratio, 0, 1)})`;
}

function getCurrentPanel() {
  const anchor = window.innerHeight * 0.42;
  let current = panels[0];

  panels.forEach((panel) => {
    if (panel.getBoundingClientRect().top <= anchor) {
      current = panel;
    }
  });

  return current;
}

function setActiveScene(panel) {
  if (!panel) return;

  const id = panel.id;
  document.body.dataset.scene = panel.dataset.story || id;

  panels.forEach((item) => {
    item.classList.toggle("is-current", item === panel);
  });

  navLinks.forEach((link) => {
    link.classList.toggle("is-active", link.getAttribute("href") === `#${id}`);
  });

  navGroups.forEach((group) => {
    const hasActiveLink = [...group.querySelectorAll("a")].some((link) => link.getAttribute("href") === `#${id}`);
    group.classList.toggle("is-active", hasActiveLink);
  });

  railLinks.forEach((link) => {
    link.classList.toggle("is-active", link.dataset.rail === id);
  });
}

function updateActiveScene() {
  setActiveScene(getCurrentPanel());
}

languageButtons.forEach((button) => {
  button.addEventListener("click", () => {
    applyLanguage(button.dataset.lang);
    updateProgress();
    updateActiveScene();
  });
});


function closeNavGroups(exceptGroup = null) {
  navGroups.forEach((group) => {
    if (group === exceptGroup) return;
    group.classList.remove("is-open");
    group.querySelector(".nav-group-trigger")?.setAttribute("aria-expanded", "false");
  });
}

navGroups.forEach((group) => {
  const trigger = group.querySelector(".nav-group-trigger");
  if (!trigger) return;

  trigger.addEventListener("click", (event) => {
    event.stopPropagation();
    const willOpen = !group.classList.contains("is-open");
    closeNavGroups(willOpen ? group : null);
    group.classList.toggle("is-open", willOpen);
    trigger.setAttribute("aria-expanded", String(willOpen));
  });
});

document.addEventListener("click", (event) => {
  if (!event.target.closest(".site-menu")) {
    closeNavGroups();
  }
});

document.addEventListener("keydown", (event) => {
  if (event.key === "Escape") {
    closeNavGroups();
    siteMenu?.classList.remove("is-open");
    menuToggle?.setAttribute("aria-expanded", "false");
  }
});
if (menuToggle && siteMenu) {
  menuToggle.addEventListener("click", () => {
    const isOpen = siteMenu.classList.toggle("is-open");
    if (!isOpen) closeNavGroups();
    menuToggle.setAttribute("aria-expanded", String(isOpen));
  });

  siteMenu.addEventListener("click", (event) => {
    if (event.target instanceof HTMLAnchorElement) {
      closeNavGroups();
      siteMenu.classList.remove("is-open");
      menuToggle.setAttribute("aria-expanded", "false");
    }
  });
}

if ("IntersectionObserver" in window) {
  const revealObserver = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("is-visible");
        revealObserver.unobserve(entry.target);
      }
    });
  }, { rootMargin: "0px 0px -12% 0px", threshold: 0.16 });

  revealItems.forEach((item) => revealObserver.observe(item));

  const panelObserver = new IntersectionObserver((entries) => {
    const visible = entries
      .filter((entry) => entry.isIntersecting)
      .sort((a, b) => b.intersectionRatio - a.intersectionRatio)[0];

    if (visible) {
      setActiveScene(visible.target);
    }
  }, { threshold: [0.36, 0.52, 0.68] });

  panels.forEach((panel) => panelObserver.observe(panel));
} else {
  revealItems.forEach((item) => item.classList.add("is-visible"));
}

window.addEventListener("scroll", () => {
  updateProgress();
  updateActiveScene();
}, { passive: true });

window.addEventListener("resize", () => {
  updateProgress();
  updateActiveScene();
});

